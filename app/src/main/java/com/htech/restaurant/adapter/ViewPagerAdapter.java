package com.htech.restaurant.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import com.htech.restaurant.fragment.CategoriesFragment;
import com.htech.restaurant.fragment.FavoriteFragment;
import com.htech.restaurant.fragment.OrderFragment;

/**
 * Created by software on 7/29/15.
 * https://guides.codepath.com/android/Sliding-Tabs-with-PagerSlidingTabStrip
 *
 * http://stackoverflow.com/questions/7263291/viewpager-pageradapter-not-updating-the-view
 */
public class ViewPagerAdapter extends FragmentStatePagerAdapter {

    CharSequence Titles[]; // This will Store the Titles of the Tabs which are Going to be passed when ViewPagerAdapter is created
    int NumbOfTabs; // Store the number of tabs, this will also be passed when the ViewPagerAdapter is created


    // Build a Constructor and assign the passed Values to appropriate values in the class
    public ViewPagerAdapter(FragmentManager fm, CharSequence mTitles[], int mNumbOfTabsumb) {
        super(fm);

        this.Titles = mTitles;
        this.NumbOfTabs = mNumbOfTabsumb;

    }

    //This method return the fragment for the every position in the View Pager
    @Override
    public Fragment getItem(int position) {

        if (position == 0) // if the position is 0 we are returning the First tab
        {
            FavoriteFragment favoriteFragment = new FavoriteFragment();
            return favoriteFragment;
        } else if (position == 1) {
            CategoriesFragment categoriesFragment = new CategoriesFragment();
            return categoriesFragment;
        } else {
            OrderFragment orderFragment = new OrderFragment();
            return orderFragment;
        }

    }
    // This method return the titles for the Tabs in the Tab Strip

    @Override
    public CharSequence getPageTitle(int position) {
        return Titles[position];
    }

    // This method return the Number of tabs for the tabs Strip

    @Override
    public int getCount() {
        return NumbOfTabs;
    }

    @Override
    public int getItemPosition(Object object) {
        return POSITION_NONE;
    }
}